/*******************************************************************************
* Function Name  : TIM2_IRQHandler
* Description    : 定时时间为1ms
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void TIM2_IRQHandler(void)
{   
    if(TIM_GetITStatus(TIM2, TIM_IT_Update) == SET)//定时时间已经到达
    {   
        TIM_ClearFlag(TIM2, TIM_FLAG_Update);//清除定时中断标志
        
        /*测试用 定时器是否有效
        gg++;
        if(gg >= 1000)
        {
            gg = 0;
            Uart1_SendByte(0xcc);
        }
	*/
//------------------------------------------------------------------------------        
  
//------------------------------------------------------------------------------        
      if(usb_flag != 0)
          usb_flag--;

      time_now++;
        //以下为串口1传输报文时间
        if(Uart1_StartFlag)
        {
            Uart1_StartFlag = FALSE;
            Uart1_On = TRUE;
        }
        if(Uart1_On)
	{
	    Uart1_ByteTime++;
            if(Uart1_ByteTime >= 40) //如果超时
	    {
                 Uart1_EndFlag = TRUE;  //Uart单字节传输结束标志 
	         Uart1_On = FALSE;
	    }
	}
    }    
}

 

